package jagerfield.expense.sharing.Retrofit;

public class User
{
    private String id;
    private String email;
    private String name;
    private String image;
    private String mobile;

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getMobile() {
        return mobile;
    }

}